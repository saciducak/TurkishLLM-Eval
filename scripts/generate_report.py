#!/usr/bin/env python3
"""
Generate HTML/Markdown evaluation report from results JSON.

Usage:
    python scripts/generate_report.py results/eval_trendyol-7b_20240101.json
"""

import argparse
import json
import sys
from pathlib import Path
from datetime import datetime

sys.path.insert(0, str(Path(__file__).parent.parent))

from rich.console import Console
from rich.table import Table
from rich.panel import Panel

console = Console()


def generate_markdown_report(results: dict, output_path: Path) -> None:
    """Generate a Markdown report from evaluation results."""
    model_name = results.get("display_name", results.get("model_name", "Unknown"))
    timestamp = results.get("timestamp", datetime.now().isoformat())
    turkeval = results.get("turkeval_score", 0)
    grade = results.get("turkeval_grade", "N/A")

    lines = [
        f"# 🇹🇷 TurkishLLM-Eval Report",
        f"",
        f"**Model:** {model_name}  ",
        f"**Developer:** {results.get('developer', 'Unknown')}  ",
        f"**Parameters:** {results.get('parameters', 'Unknown')}  ",
        f"**Date:** {timestamp}  ",
        f"**TurkEval™ Score:** {turkeval:.1f} ({grade})",
        f"",
        f"---",
        f"",
        f"## Benchmark Results",
        f"",
        f"| Benchmark | Score | Accuracy |",
        f"|-----------|-------|----------|",
    ]

    for bname, bdata in results.get("benchmarks", {}).items():
        if isinstance(bdata, dict):
            score = bdata.get("score", 0)
            acc = bdata.get("accuracy_pct", 0)
            lines.append(f"| {bname} | {score:.4f} | {acc:.1f}% |")

    lines.extend([
        f"",
        f"## Category Breakdown",
        f"",
    ])

    for bname, bdata in results.get("benchmarks", {}).items():
        if isinstance(bdata, dict) and "category_scores" in bdata:
            lines.append(f"### {bname}")
            lines.append(f"| Category | Score |")
            lines.append(f"|----------|-------|")
            for cat, score in bdata["category_scores"].items():
                lines.append(f"| {cat} | {score:.4f} |")
            lines.append("")

    if "judge_stats" in results:
        js = results["judge_stats"]
        lines.extend([
            f"## Judge Pipeline Stats",
            f"- **Strategy:** {js.get('strategy', 'N/A')}",
            f"- **Judges:** {js.get('num_judges', 0)}",
            f"- **Total Evaluations:** {js.get('total_evaluations', 0)}",
            f"- **Avg Agreement:** {js.get('avg_agreement', 0):.4f}",
            f"- **Cohen's κ:** {js.get('cohens_kappa', 0):.4f}",
            f"",
        ])

    lines.append(f"\n---\n*Generated by TurkishLLM-Eval v0.1.0*")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    console.print(f"[green]Report saved to: {output_path}[/green]")


def main():
    parser = argparse.ArgumentParser(description="Generate evaluation report")
    parser.add_argument("results_file", type=str, help="Path to results JSON")
    parser.add_argument("--output", type=str, default=None, help="Output path")
    parser.add_argument("--format", type=str, default="markdown", choices=["markdown"])
    args = parser.parse_args()

    results_path = Path(args.results_file)
    if not results_path.exists():
        console.print(f"[red]File not found: {results_path}[/red]")
        sys.exit(1)

    with open(results_path, "r", encoding="utf-8") as f:
        results = json.load(f)

    output_path = Path(args.output) if args.output else results_path.with_suffix(".md")
    generate_markdown_report(results, output_path)

    # Also print summary to console
    table = Table(title=f"📊 {results.get('display_name', 'Model')} — TurkEval™: {results.get('turkeval_score', 0):.1f}")
    table.add_column("Benchmark", style="cyan")
    table.add_column("Score", style="green")
    for bname, bdata in results.get("benchmarks", {}).items():
        if isinstance(bdata, dict):
            table.add_row(bname, f"{bdata.get('score', 0):.4f}")
    console.print(table)


if __name__ == "__main__":
    main()
